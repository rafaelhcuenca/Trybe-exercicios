let saldo = 100;

function somaSaldo(valor) {
  return valor + saldo;
}
console.log(somaSaldo(200));

function subSaldo(valor) {
  return valor - saldo;
}
console.log(subSaldo(128));

function multSaldo(valor) {
  return valor * saldo;
}
console.log(multSaldo(45));

function divSaldo(valor) {
  return valor / saldo;
}
console.log(divSaldo(59));


